package model;

public class Attendence {
    private String stdId;
    private String date;
    private String time;

    public String getStdId() {
        return stdId;
    }

    public void setStdId(String stdId) {
        this.stdId = stdId;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public Attendence(String stdId, String date, String time) {
        this.stdId = stdId;
        this.date = date;
        this.time = time;
    }
}
